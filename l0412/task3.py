"""Task 3. Разворот числа
--> 67
76 <--
"""
# Вариант 1
number = input('Введите двузначное число: ')
d1 = number[0]  # десятки
d2 = number[1]  # единицы
d3 = d2 + d1
print(int(d3))

# Вариант 2
num = int(input('Введите двузначное число: '))
d1 = num // 10  # десятки
d2 = num % 10  # единицы
d3 = d2 * 10 + d1
print(d3)

